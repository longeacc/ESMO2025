"""
Convert CANTEMIST35 (Brat format) -> GLiNER training format.

Uses the frozen split from Rules/src/split_train.txt and split_test.txt
(generated by cantemist_split.py).

Output files: train_gliner.json, test_gliner.json

Each sentence-level sample:
{
    "tokenized_text": ["Nous", "presentons", ...],
    "ner": [[token_start, token_end, "LABEL"], ...],
    "relations": [],
    "_source": "cc_onco13"
}

Usage:
    pip install spacy numpy
    python -m spacy download fr_core_news_sm
    python prepare_gliner_data.py
"""

